    @staticmethod
    def diacritic(word: str):
        diacritic_1_chars = ['á', 'ắ', 'ấ', 'é', 'ế', 'í', 'ó', 'ố', 'ớ', 'ú', 'ứ', 'ý']
        diacritic_2_chars = ['à', 'ằ', 'ầ', 'è', 'ề', 'ì', 'ò', 'ồ', 'ờ', 'ù', 'ừ', 'ỳ']
        diacritic_3_chars = ['ả', 'ẳ', 'ẩ', 'ẻ', 'ể', 'ỉ', 'ỏ', 'ổ', 'ở', 'ủ', 'ử', 'ỷ']
        diacritic_4_chars = ['ã', 'ẵ', 'ẫ', 'ẽ', 'ễ', 'ĩ', 'õ', 'ỗ', 'ỡ', 'ũ', 'ữ', 'ỹ']
        diacritic_5_chars = ['ạ', 'ặ', 'ậ', 'ẹ', 'ệ', 'ị', 'ọ', 'ộ', 'ợ', 'ụ', 'ự', 'ỵ']
        current_diacritic = 0
        idx_in_word = 0
        char_idx = 0
        occurences = 0
        for idx, char in enumerate(word):
            if occurences > 1:
                # Not a Vietnamese word
                return None, None, None
            if char in diacritic_1_chars:
                char_idx = diacritic_1_chars.index(char)
                occurences += 1
                current_diacritic = 1
                idx_in_word = idx
            elif char in diacritic_2_chars:
                char_idx = diacritic_2_chars.index(char)
                occurences += 1
                current_diacritic = 2
                idx_in_word = idx
            elif char in diacritic_3_chars:
                char_idx = diacritic_3_chars.index(char)
                occurences += 1
                current_diacritic = 3
                idx_in_word = idx
            elif char in diacritic_4_chars:
                char_idx = diacritic_4_chars.index(char)
                occurences += 1
                current_diacritic = 4
                idx_in_word = idx
            elif char in diacritic_5_chars:
                char_idx = diacritic_5_chars.index(char)
                occurences += 1
                current_diacritic = 5
                idx_in_word = idx
        
        if current_diacritic == 0:
            idx_in_word, char_idx = None, None
            
        return current_diacritic, idx_in_word, char_idx
            
    @staticmethod
    def consonant_rhymeFamily_tone(word: str):
        original_word = word
        diacritic, idx_in_word, char_idx = Vietnamese.diacritic(word)
        if diacritic is None:
            return original_word, None, None
        
        # Replace character with diacritic with its non-diacritic counterpart
        if idx_in_word is not None:
            word = word[:idx_in_word] + Vietnamese.diacritic_0_chars[char_idx] + word[idx_in_word+1:]
        original_word_no_diacritic = word
        
